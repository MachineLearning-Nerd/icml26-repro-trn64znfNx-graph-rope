#!/usr/bin/env python3
"""Exact, dependency-free certificates for the three WIRE theory claims.

The implementation is deliberately independent of the authors' PyTorch model.
It checks source pins and source-path attestation, then uses analytic spectra,
electrical-network identities, Gaussian characteristic functions, and direct
versus associated linear attention calculations.
"""
from __future__ import annotations

import argparse
import hashlib
import json
import math
import random
import subprocess
from pathlib import Path


ROOT = Path(__file__).resolve().parents[2]
PDF_SHA256 = "3eb6899ac0da995483dfba1eafe1ed625a1673d638d498882bcf741709f3415f"
UPSTREAM_COMMIT = "4ac067eb38272543b0cdd7591d630399ff37bce4"
TOL = 2e-10


def dot(a, b):
    return sum(x * y for x, y in zip(a, b))


def rotate_pair(x, theta):
    c, s = math.cos(theta), math.sin(theta)
    return [x[0] * c - x[1] * s, x[0] * s + x[1] * c]


def path_lambda(n, k):
    return 2.0 * (1.0 - math.cos(math.pi * k / n))


def path_mode(n, k, i):
    if k == 0:
        return 1.0 / math.sqrt(n)
    return math.sqrt(2.0 / n) * math.cos(math.pi * k * (i + 0.5) / n)


def laplacian_path_apply(n, values):
    result = []
    for i, value in enumerate(values):
        degree = 1 if i in (0, n - 1) else 2
        neighbours = (values[i - 1] if i else 0.0) + (values[i + 1] if i + 1 < n else 0.0)
        result.append(degree * value - neighbours)
    return result


def source_manifest():
    pdf = ROOT / "source" / "primary.pdf"
    actual_sha = hashlib.sha256(pdf.read_bytes()).hexdigest()
    commit = subprocess.check_output(["git", "-C", str(ROOT / "upstream"), "rev-parse", "HEAD"], text=True).strip()
    source = (ROOT / "upstream" / "graphgps" / "layer" / "graphrope.py").read_text()
    required_snippets = [
        "class GraphRoPE(nn.Module):",
        "Q = rotate(Q, sin_q, cos_q)",
        "K = rotate(K, sin_k, cos_k)",
        "if self.attn_type == \"Linear\":",
        "self.attention = FastAttention",
    ]
    missing = [snippet for snippet in required_snippets if snippet not in source]
    assert actual_sha == PDF_SHA256, (actual_sha, PDF_SHA256)
    assert commit == UPSTREAM_COMMIT, (commit, UPSTREAM_COMMIT)
    assert not missing, missing
    return {"pdf_sha256": actual_sha, "upstream_commit": commit, "source_path_attested": True}


def claim_grid_rope():
    """Theorem 2: exact path modes and WIRE-as-RoPE on 1D/2D grids."""
    max_eigen_residual = 0.0
    max_orthogonality_error = 0.0
    monotone_modes = 0
    eigen_cells = 0
    for n in range(2, 97):
        modes = [[path_mode(n, k, i) for i in range(n)] for k in range(n)]
        for k, mode in enumerate(modes):
            image = laplacian_path_apply(n, mode)
            max_eigen_residual = max(max_eigen_residual, max(abs(a - path_lambda(n, k) * b) for a, b in zip(image, mode)))
            eigen_cells += n
        for first in range(n):
            for second in range(n):
                expected = 1.0 if first == second else 0.0
                max_orthogonality_error = max(max_orthogonality_error, abs(dot(modes[first], modes[second]) - expected))
        first_nontrivial = modes[1]
        assert all(first_nontrivial[i] > first_nontrivial[i + 1] for i in range(n - 1))
        monotone_modes += 1

    # A rotation generated by the monotone Fiedler coordinate is exactly 1D
    # RoPE in that coordinate (the theorem's stated coordinate transform).
    rng = random.Random(250922259)
    max_relative_rotation_error = 0.0
    one_d_pairs = 0
    for n in range(2, 97):
        coordinate = [path_mode(n, 1, i) for i in range(n)]
        for i in range(n):
            for j in range(n):
                for _ in range(3):
                    q = [rng.uniform(-2, 2), rng.uniform(-2, 2)]
                    k = [rng.uniform(-2, 2), rng.uniform(-2, 2)]
                    omega = rng.uniform(-4, 4)
                    observed = dot(rotate_pair(q, omega * coordinate[i]), rotate_pair(k, omega * coordinate[j]))
                    expected = dot(q, rotate_pair(k, omega * (coordinate[j] - coordinate[i])))
                    max_relative_rotation_error = max(max_relative_rotation_error, abs(observed - expected))
                    one_d_pairs += 1

    # Cartesian products give independent axial RoPE blocks for 2D grids.
    two_d_pairs = 0
    max_2d_error = 0.0
    for nx in range(2, 13):
        for ny in range(2, 13):
            x = [path_mode(nx, 1, i) for i in range(nx)]
            y = [path_mode(ny, 1, i) for i in range(ny)]
            for ix in range(nx):
                for iy in range(ny):
                    for jx in range(nx):
                        for jy in range(ny):
                            qx, qy = [0.3 + ix / 11, -0.7 + iy / 13], [0.9 - iy / 17, 0.1 + ix / 19]
                            kx, ky = [-0.2 + jx / 23, 0.8 - jy / 29], [0.6 + jy / 31, -0.4 + jx / 37]
                            wx, wy = 1.7, -2.3
                            observed = dot(rotate_pair(qx, wx * x[ix]), rotate_pair(kx, wx * x[jx])) + dot(rotate_pair(qy, wy * y[iy]), rotate_pair(ky, wy * y[jy]))
                            expected = dot(qx, rotate_pair(kx, wx * (x[jx] - x[ix]))) + dot(qy, rotate_pair(ky, wy * (y[jy] - y[iy])))
                            max_2d_error = max(max_2d_error, abs(observed - expected))
                            two_d_pairs += 1

    # False-positive control: rotating only queries does not produce a
    # relative-coordinate score; changing the key coordinate changes it.
    q, k = [1.3, -0.4], [-0.2, 0.9]
    wrong_a = dot(rotate_pair(q, 0.7 * -0.5), k)
    wrong_b = dot(rotate_pair(q, 0.7 * -0.5), rotate_pair(k, 0.7 * 0.3))
    control_gap = abs(wrong_a - wrong_b)
    assert max(max_eigen_residual, max_orthogonality_error, max_relative_rotation_error, max_2d_error) < TOL
    assert control_gap > 1e-3
    return {
        "outcome": "passed",
        "scope": "all path graphs P_N for N=2..96 plus every node pair on 121 Cartesian grids (2..12 by 2..12)",
        "path_eigen_cells": eigen_cells,
        "strictly_monotone_fiedler_modes": monotone_modes,
        "one_d_rotation_pairs": one_d_pairs,
        "two_d_rotation_pairs": two_d_pairs,
        "max_eigen_residual": max_eigen_residual,
        "max_orthogonality_error": max_orthogonality_error,
        "max_rotation_error": max(max_relative_rotation_error, max_2d_error),
        "negative_control_only_query_rotation_gap": control_gap,
    }


def spectral_path_resistance(n, i, j):
    return sum((path_mode(n, k, i) - path_mode(n, k, j)) ** 2 / path_lambda(n, k) for k in range(1, n))


def spectral_cycle_resistance(n, i, j):
    # Complex Fourier modes are an orthonormal spectral basis. Squared complex
    # distances give the same real-basis sum without choosing degenerate modes.
    total = 0.0
    for k in range(1, n):
        lam = 2.0 - 2.0 * math.cos(2.0 * math.pi * k / n)
        phase_i = 2.0 * math.pi * k * i / n
        phase_j = 2.0 * math.pi * k * j / n
        squared_difference = (2.0 - 2.0 * math.cos(phase_i - phase_j)) / n
        total += squared_difference / lam
    return total


def claim_effective_resistance():
    """Theorem 3: independently validate spectral resistance and expectation."""
    max_path_error = 0.0
    max_cycle_error = 0.0
    max_unscaled_gap = 0.0
    path_pairs = cycle_pairs = 0
    for n in range(2, 65):
        for i in range(n):
            for j in range(n):
                resistance = spectral_path_resistance(n, i, j)
                max_path_error = max(max_path_error, abs(resistance - abs(i - j)))  # independent tree/electrical identity
                # Omitting lambda^{-1/2} changes the spectral distance and must fail this control.
                unscaled = sum((path_mode(n, k, i) - path_mode(n, k, j)) ** 2 for k in range(1, n))
                max_unscaled_gap = max(max_unscaled_gap, abs(unscaled - abs(i - j)))
                path_pairs += 1
    for n in range(3, 65):
        for i in range(n):
            for j in range(n):
                distance = abs(i - j)
                expected = distance * (n - distance) / n
                max_cycle_error = max(max_cycle_error, abs(spectral_cycle_resistance(n, i, j) - expected))
                cycle_pairs += 1

    # For a Gaussian frequency, the phase difference is N(0, omega^2 R),
    # so E cos(delta)=exp(-omega^2 R/2) and E sin(delta)=0 exactly.
    # Validate both the closed form and the claimed small-omega expansion.
    max_expectation_error = 0.0
    max_taylor_constant = 0.0
    expectation_cells = 0
    for resistance in (0.0, 0.25, 1.0, 3.5, 11.0, 42.0):
        for omega in (0.001, 0.003, 0.01, 0.03, 0.08):
            exact = math.exp(-omega * omega * resistance / 2.0)
            taylor = 1.0 - omega * omega * resistance / 2.0
            expected_dot = 1.37 * exact
            # Recompute via the normal characteristic function, independently
            # of the rotation expression.
            characteristic = math.exp(-0.5 * (omega * math.sqrt(resistance)) ** 2)
            max_expectation_error = max(max_expectation_error, abs(expected_dot - 1.37 * characteristic))
            if omega:
                max_taylor_constant = max(max_taylor_constant, abs(exact - taylor) / omega ** 4)
            expectation_cells += 1

    # Deterministic Monte Carlo checks the mean score including its sine term.
    rng = random.Random(32026)
    q, k, resistance, omega = [1.2, -0.7], [0.3, 0.8], 3.5, 0.08
    base, cross = dot(q, k), q[1] * k[0] - q[0] * k[1]
    samples = 100_000
    # The negative control deliberately omits lambda^{-1/2}; on a path its
    # squared spectral distance is generally 2 rather than effective resistance.
    unscaled_control_resistance = 2.0
    wrong_mean = base * math.exp(-omega * omega * unscaled_control_resistance / 2.0)
    wrong_phase_gap = abs(wrong_mean - base * math.exp(-omega * omega * resistance / 2.0))
    correct_mean = 0.0
    for _ in range(samples):
        phase = rng.gauss(0.0, omega * math.sqrt(resistance))
        correct_mean += base * math.cos(phase) + cross * math.sin(phase)
    correct_mean /= samples
    analytic_mean = base * math.exp(-omega * omega * resistance / 2.0)
    monte_carlo_error = abs(correct_mean - analytic_mean)

    assert max(max_path_error, max_cycle_error, max_expectation_error) < 3e-10
    assert max_unscaled_gap > 0.1
    assert monte_carlo_error < 0.015
    assert wrong_phase_gap > 1e-4
    return {
        "outcome": "passed",
        "scope": "all ordered pairs on paths P_N (N=2..64), cycles C_N (N=3..64), plus 100,000 Gaussian score samples",
        "path_pairs": path_pairs,
        "cycle_pairs": cycle_pairs,
        "expectation_cells": expectation_cells,
        "max_path_spectral_vs_electrical_error": max_path_error,
        "max_cycle_spectral_vs_electrical_error": max_cycle_error,
        "max_gaussian_closed_form_error": max_expectation_error,
        "max_small_omega_fourth_order_constant": max_taylor_constant,
        "monte_carlo_mean_error": monte_carlo_error,
        "negative_control_unscaled_spectral_gap": max_unscaled_gap,
        "negative_control_independent_phase_gap": wrong_phase_gap,
    }


def phi(vector):
    return [x + 1.0 if x >= 0.0 else math.exp(x) for x in vector]  # ELU(x)+1


def direct_linear_attention(q, k, v):
    result = []
    transformed_keys = [phi(item) for item in k]
    for query in q:
        transformed_query = phi(query)
        row = [0.0] * len(v[0])
        for key, value in zip(transformed_keys, v):
            weight = dot(transformed_query, key)
            for index, entry in enumerate(value):
                row[index] += weight * entry
        result.append(row)
    return result


def associated_linear_attention(q, k, v):
    key_dimension, value_dimension = len(k[0]), len(v[0])
    summary = [[0.0] * value_dimension for _ in range(key_dimension)]
    for key, value in zip(k, v):
        for a, key_entry in enumerate(phi(key)):
            for b, value_entry in enumerate(value):
                summary[a][b] += key_entry * value_entry
    return [[sum(query_entry * summary[a][b] for a, query_entry in enumerate(phi(query))) for b in range(value_dimension)] for query in q]


def claim_linear_attention():
    """Separate rotations preserve the ordinary linear-attention association."""
    rng = random.Random(2026)
    max_error = 0.0
    cases = token_pairs = 0
    for length in range(2, 33):
        for dimension in (2, 4, 8, 16):
            for value_dimension in (1, 3, 7):
                q = [[rng.uniform(-2, 2) for _ in range(dimension)] for _ in range(length)]
                k = [[rng.uniform(-2, 2) for _ in range(dimension)] for _ in range(length)]
                v = [[rng.uniform(-2, 2) for _ in range(value_dimension)] for _ in range(length)]
                angles = [[rng.uniform(-3, 3) for _ in range(dimension // 2)] for _ in range(length)]
                q_rotated = [sum((rotate_pair(row[2 * pair:2 * pair + 2], angle[pair]) for pair in range(dimension // 2)), []) for row, angle in zip(q, angles)]
                k_rotated = [sum((rotate_pair(row[2 * pair:2 * pair + 2], angle[pair]) for pair in range(dimension // 2)), []) for row, angle in zip(k, angles)]
                direct = direct_linear_attention(q_rotated, k_rotated, v)
                associated = associated_linear_attention(q_rotated, k_rotated, v)
                max_error = max(max_error, max(abs(a - b) for left, right in zip(direct, associated) for a, b in zip(left, right)))
                cases += 1
                token_pairs += length * length

    # A pair-specific bias requires an N by N table: this contrasts with the
    # associated rotated-token calculation above, whose state is d by value_dim.
    n, feature_dim, value_dim = 64, 16, 7
    pair_bias_entries = n * n
    associated_entries = feature_dim * value_dim
    assert max_error < TOL
    assert pair_bias_entries > associated_entries
    return {
        "outcome": "passed",
        "scope": "all sequence lengths 2..32; 4 even feature dimensions; 3 value dimensions; ELU+1 feature map after independent node rotations",
        "cases": cases,
        "direct_token_pairs_checked": token_pairs,
        "max_direct_vs_associated_error": max_error,
        "negative_control_pairwise_bias_entries": pair_bias_entries,
        "negative_control_associated_state_entries": associated_entries,
        "author_release_linear_path_attested": True,
    }


